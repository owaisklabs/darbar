                function calculateTotal(e) {
                    qty = $(e).val()
                    window.x += 1
                    var z = window.x + 1
                    var newId = e.id + z;
                    $(e).attr("id", newId)
                    price = $('#' + newId).closest('div').prev().find('.rate').val();
                    sum = parseInt(price) * parseInt(qty)
                    $('#' + newId).closest('div').next().find('.total').val(sum);

                }

                function calculateGrandTotal() {
                    var sum = 0;
                    $('.total').each(function() {
                        sum += parseFloat($(this).val()); // Or this.innerHTML, this.innerText
                    });
                    $('#grandtotal').text(sum.toString())
                }

